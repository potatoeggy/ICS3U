import java.util.Scanner;

class While4 {
	public static void main(String[] args) {
		// Initialise counter variable in place of for loop
		int counter = 1;
		while (counter <= 10) {
			System.out.println(counter + ". Daniel");
			counter++;		
		}
	}
}
