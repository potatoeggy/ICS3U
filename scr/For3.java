// Count to 100 and down

class For3 { 
	public static void main(String[] args) {
		// while i is less than 100, starting at 0, increasing by 1
		for (int i = 0; i < 100; i++) {
			System.out.println(i);
		}
		for (int i = 100; i > -1; i--) {
			System.out.println(i);
		}
	}
}
